#include "libft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	j;

	i = ft_strlen(s2);
	j = ft_strlen(s1);
	if (j != i)
		return (54);
	i = 0;
	while (s1[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (s1[i] - s2[i]);
}
