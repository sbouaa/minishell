#include "libft.h"

char	*ft_strdup(const char	*s1)
{
	int		i;
	char	*p;

	p = g_malloc(ft_strlen(s1) + 1, MALLOC);
	if (!p)
		return (NULL);
	i = -1;
	while (s1[++i])
		p[i] = s1[i];
	p[i] = '\0';
	return (p);
}
